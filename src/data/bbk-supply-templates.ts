/**
 * Calculate the target quantity for an item based on household size.
 * - per_person items scale with the number of persons
 * - per_household items remain at the base quantity regardless of household size
 */
export function calculateTargetQty(item: BbkSupplyItem, persons: number): number {
  if (item.scaleType === 'per_household') return item.qtyPerPerson10Days
  return Math.ceil(item.qtyPerPerson10Days * persons)
}
