// Based on: Bundesamt für Bevölkerungsschutz und Katastrophenhilfe (BBK)
// "Checklisten zur Vorsorge — Krisen und Katastrophen" (offizielle BBK-Checkliste)
// Alle Items aus dem offiziellen BBK-PDF sind enthalten.

export type SupplyCategory =
  | 'getraenke'
  | 'lebensmittel'
  | 'hygiene'
  | 'medikamente'
  | 'notfallausruestung'
  | 'werkzeuge'
  | 'dokumente'
  | 'babybedarf'
  | 'tierbedarf'

export interface BbkSupplyItem {
  category: SupplyCategory
  subcategory?: string
  itemName: string
  qtyPerPerson10Days: number // base quantity per person for 10 days
  unit: string // 'L', 'kg', 'g', 'Stk', 'Pkg', 'Rollen', 'Paar'
  scaleType: 'per_person' | 'per_household' // Taschenlampe = per_household, Wasser = per_person
  isEssential: boolean // core BBK item vs nice-to-have
  estimatedPricePerUnit?: number // EUR Richtwert pro Einheit (für Einkaufsliste)
  notes?: string
}

export interface BbkCategoryConfig {
  id: SupplyCategory
  name: string
  icon: string // lucide-react icon name as string
  color: string // tailwind color prefix like 'blue', 'green', 'amber'
  items: BbkSupplyItem[]
  conditional?: 'babies' | 'pets' // only shown if household has babies/pets
}

